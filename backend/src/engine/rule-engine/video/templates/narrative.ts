// ─── Narrative Video Formula Template ──────────────────────────────────────────
// Motion Formula v2.0 — character-driven shots. Holds the shared section
// builders reused across all video categories: the 15-section core structure
// (shot type through physics/motion dynamics) plus the 6-section Advanced
// Enhancement layer (visual detail through story telling) that's always
// assembled, never optional.

import { VideoCategory, VideoCategoryTemplate } from "../types.js"
import {
  MICRO_ACTION_BY_CATEGORY, QUALITY_TAG_BY_CATEGORY, AUDIO_BY_CATEGORY,
  ASPECT_RATIO_BY_CATEGORY, RENDER_ENGINE_BY_CATEGORY, MOOD_ATMOSPHERE_BY_CATEGORY,
  VISUAL_DETAIL_BY_CATEGORY, STORY_TELLING_BY_CATEGORY, CATEGORY_DEFAULTS,
} from "../dictionaries.js"

export const NARRATIVE_TEMPLATE: VideoCategoryTemplate = {
  name: "Narrative / Character",
  defaultCameraMove: "steadicam",
  defaultLighting: "practical interior",
  defaultDuration: "5-10s",
  sections: [
    "SHOT_TYPE", "SUBJECT", "ACTION_MICRO", "ENVIRONMENT", "LIGHTING_GEOMETRY",
    "CAMERA_MOVEMENT", "TIME_WEATHER", "STYLE", "QUALITY_TAG", "AUDIO",
    "ASPECT_RATIO", "DURATION", "COLOR_GRADE_RENDER", "MOOD_ATMOSPHERE", "PHYSICS_MOTION",
    "VISUAL_DETAIL", "CINEMATOGRAPHY", "REALISM", "SCENE_CONSISTENCY", "MOTION_QUALITY", "STORY_TELLING",
  ],
}

// Strips the "**LOCKS - X:** " label prefix off a lock sentence generated by
// lock-generator.ts, so its content can be folded under a v2.0 section label
// instead of the old end-of-prompt appendix convention.
function stripLockLabel(lockText: string): string {
  return lockText.replace(/^\*\*LOCKS - [A-Z]+:\*\*\s*/, "")
}

// ─── Core section builders (1-15, shared across all video categories) ────────

export function buildShotTypeSection(shotType: string): string {
  return `SHOT TYPE: ${shotType}`
}

