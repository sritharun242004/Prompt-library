export function buildCinematographySection(mood: string = "cinematic"): string {
  const refs: Record<string, string> = {
    "cinematic":   "CINEMATOGRAPHY: Roger Deakins — naturalistic motivated light. Emmanuel Lubezki — continuous immersive camera movement. Greig Fraser — high-contrast atmospheric depth",
    "documentary": "CINEMATOGRAPHY: handheld observational style, natural available light, unobtrusive camera presence, authentic unscripted feel",
    "commercial":  "CINEMATOGRAPHY: clean confident camera moves, glossy controlled lighting, brand-forward polish, high production value",
    "dreamlike":   "CINEMATOGRAPHY: Wong Kar-wai — saturated slow-motion intimacy. Terrence Malick — natural light, flowing camera, poetic pacing",
  }
  return refs[mood] ?? refs["cinematic"]
}

